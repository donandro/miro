package com.miro.task.models;

public class CreateWidgetRequest {
	
	private int x;
	
    private int y;
    
    private Integer zIndex;
    
    private int width;
    
    private int height;

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public Integer getzIndex() {
		return zIndex;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}
}
